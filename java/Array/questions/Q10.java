/*
  Write a Java program to print the 2nd last element of an array.
  Input-{1,2,3,4,5,6}
    Output-{5}
 */
package questions;

public class Q10 {

    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5,6};

        System.out.println("2nd Last element: "+arr[arr.length-2]);
    }
    
}
